# 🎁 Class & object

"""
Python is an object oriented programming language. Everything in python is an object.
"""

#🚀 Create a Class
# we create a class using the "class" keyword, followed by the name of the class.

class myClass:
    # myClass is the class name
    x = 5
    # we call variable as "properties" in OOP

#🚀 Create an Object
# we can use the class name to create objects and store it in a variable.

myObj = myClass()
print(myObj.x)